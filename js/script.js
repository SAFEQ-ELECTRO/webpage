// ========== HIDE NAVBAR ON SCROLL ==========
let lastScrollPosition = 0;
const navbarElement = document.getElementById('navbar');
const topBarElement = document.querySelector('.top-bar');

function getNavbarThreshold() {
  const topBarHeight = topBarElement ? topBarElement.offsetHeight : 0;
  const navbarHeight = navbarElement ? navbarElement.offsetHeight : 0;
  return topBarHeight + navbarHeight;
}

function handleNavbarScroll() {
  if (!navbarElement) return;
  
  const currentScrollPosition = window.pageYOffset || document.documentElement.scrollTop;
  const scrollThreshold = getNavbarThreshold();
  
  // Scrolling down & past threshold
  if (currentScrollPosition > lastScrollPosition && currentScrollPosition > scrollThreshold) {
    navbarElement.style.transform = 'translateY(-100%)';
  } 
  // Scrolling up
  else if (currentScrollPosition < lastScrollPosition) {
    navbarElement.style.transform = 'translateY(0)';
  }
  // At the very top
  else if (currentScrollPosition <= 10) {
    navbarElement.style.transform = 'translateY(0)';
  }
  
  lastScrollPosition = currentScrollPosition <= 0 ? 0 : currentScrollPosition;
}

// Debounced scroll handler
let ticking = false;
window.addEventListener('scroll', () => {
  if (!ticking) {
    requestAnimationFrame(() => {
      handleNavbarScroll();
      ticking = false;
    });
    ticking = true;
  }
});

